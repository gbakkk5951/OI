	int dp[MXN][(int)(MXN / PI) + 10];
	int dfs(int nd, int minus) {
		if (dp[nd][minus]) return dp[nd][minus];
		if (nd - minus * PI < 4) return dp[nd][minus] = 1;
		return dp[nd][minus] = (dfs(nd - 1, minus) + dfs(nd, minus + 1)) % MOD; 
	}
